public class AssignmentOperators {
    public static void main(String[] args) {
        int a = 21;

        a += 7;
        System.out.println("a += 7: " + a);

        a -= 5;
        System.out.println("a -= 5: " + a);

        a *= 2;
        System.out.println("a *= 2: " + a);

        a /= 7;
        System.out.println("a /= 7: " + a);

        a %= 6;
        System.out.println("a %= 6: " + a);
    }
}
