public class RelationalOperators {
    public static void main(String[] args) {
        int a = 35;
        int b = 21;

        System.out.println(a +" == " +b +" is " +(a == b));
        System.out.println(a +" !=  " +b +" is " + (a != b));
        System.out.println(a +" > " +b +" is " + (a > b));
        System.out.println(a +" < " +b +" is " + (a < b));
        System.out.println(a +" >= " +b +" is " + (a >= b));
        System.out.println(a +" <= " +b +" is " + (a <= b));
    }
}
